package com.havaz.transport.api.form.location;

public class LocationRequest {
    private Point from;
    private Point to;

    public Point getFrom() {
        return from;
    }

    public void setFrom(Point from) {
        this.from = from;
    }

    public Point getTo() {
        return to;
    }

    public void setTo(Point to) {
        this.to = to;
    }
}

